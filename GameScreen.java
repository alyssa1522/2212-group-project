      private void jButton25ActionPerformed(java.awt.event.ActionEvent evt) {                                          
          // Game Screen: Check Answer
          audioPlayer.playButtonClickSound();
          int answer;
          String aInput = jTextField3.getText();
          
          answer = switch (category) {
              case " + " -> firstRandNum + secondRandNum;
              case " - " -> firstRandNum - secondRandNum;
              case " × " -> firstRandNum * secondRandNum;
              default -> firstRandNum / secondRandNum;
          };
  
          int aTest = Integer.parseInt(aInput);
  
          boolean withinTimeLimit = time[0] > 0;
          if (answer==aTest && !isCurrentQuestionAnswered){
              // correct answer: show pizza slice
              audioPlayer.playCorrectSound();
              jLabel43.setIcon(new javax.swing.ImageIcon(getClass().getResource("/pizza-icon(1).png"))); // NOI18N
              updateScore(withinTimeLimit ? 10 : 0);
              isCurrentQuestionAnswered = true;
              goToNextQuestion();
          } else {
              //incorrect answer: show crossed pizza slice
              audioPlayer.playMadCatSound();
              jLabel43.setIcon(new javax.swing.ImageIcon(getClass().getResource("/no-pizza2.png"))); // NOI18N
              updateScore(0);
          }
      }        
      
      private void updateScore(int points) {
         if (points == -1){
             score -= score;
         }else{
             score += points;
         }
         jLabel45.setText("Score: " + score);
     
         // Updating the user.csv file as well
     
         // Verifying login and seeing if username and password are correct based on if they are in user.csv
         userName = jTextField1.getText().trim();
         String[] parts = new String[4];
         String line;
     
         try {
             // Reading file user.csv
             FileReader fr = new FileReader("UserSavedStates/" + userName + ".txt");
             BufferedReader br = new BufferedReader(fr);
             StringBuilder stringBuilder = new StringBuilder();
     
             // Reading each line in user save state txt file, if the gamename is found, update the score
             while ((line = br.readLine()) != null) {
                 // Separating gamename and other fields
                 parts = line.split(",");
                 // If the username matches, update the score
                 if (parts[0].equals(gameName)) {
                     parts[1] = String.valueOf(score);
                 }
                 // Append the modified or unmodified line to the string builder
                 stringBuilder.append(String.join(",", parts)).append("\n");
             }
     
             fr.close();
     
             // Write the modified content back to user.csv file
             BufferedWriter userWriter = new BufferedWriter(new FileWriter("UserSavedStates/" + userName + ".txt"));
             userWriter.write(stringBuilder.toString());
             userWriter.close();
         } catch (Exception e) {
             e.printStackTrace(); // Handle or log the exception
         }
     }
     
  
