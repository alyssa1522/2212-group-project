     /**
      * Generates a random math question
      * @param lNum the level number user is at
      * @param operation operation of math symbol 
      * @return a string of the randomly generated question
      */
     public String generateQuestion(int lNum, String operation){
         if (timer != null){
             timer.stop();
         }
         time[0] = 121;
         timer = new Timer(1000, new ActionListener() {
             @Override
             public void actionPerformed(ActionEvent e){
                 if (time[0] > 0){
                     time[0]--;
                 }
                 else {
                     ((Timer)e.getSource()).stop();
                 }
                 jLabel44.setText("Timer: " + time[0] + "s");
             }
         });
         timer.start();
 
         Random rand = new Random();
         int firstNumber;
        int secondNumber;
        
        firstNumber = rand.nextInt(0+(lNum*5),5+(lNum*5));
        secondNumber = rand.nextInt(5+(lNum*5));

        // Division questions will produce whole number answers
        if (operation.equals(" ÷ ")){
            // First number needs to be greater than or equal to the second number
            if (firstNumber < secondNumber || firstNumber % secondNumber != 0){
                return generateQuestion(lNum, operation);
            }
        }

        firstRandNum = firstNumber;
        secondRandNum = secondNumber;
        
        return "What is "+Integer.toString(firstNumber)+operation+Integer.toString(secondNumber)+"?";
     }
